<h2> Operadores relacionais</h2>
<hr>
<p>
    são operadores ultilizados para verificar 
    a relação (maior, menor, diferente) entre 
    valores.

</p>

<strong>operadores</strong>
<ol>

<li>> | maior</li>
<li>< | menor</li>
<li>>= maior igual</li>
<li><= | menor igual</li>
<li>== | igual</li>
<li>!= | diferente</li>
<li>=== |idêntico - compara valor e tipo</li>
<li>!== | não idêntico - compara valor e o tipo </li>
</ol>

<strong>Exemplos</strong>


<pre>
 &lt;?php
$resposta = 10 == 10;
var_dump($resposta);
?&gt;
</pre>

<pre>
 &lt;?php
$resposta = 10 == 10;
var_dump($resposta);
?&gt;
</pre>

<pre>
 &lt;?php
$resposta = 10 === 10;
var_dump($resposta);
?&gt;
</pre>

<pre>
 &lt;?php
$resposta = 10 === 10;
var_dump($resposta);
?&gt;
</pre>
